package percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanJalan=0;
        switch(jalanJalan) {
            case 1:
                System.out.println("Ngabuburit");
                break;
            case 2:
                System.out.println("Gabut");
                break;
            case 3:
                System.out.println("Kerja Kelompok");
                break;
            case 4:
                System.out.println("Auh Gelap");
                break;
            default:
                System.out.println("Ga Ada Akhlak :)");
                break;
        }
    }
}
